
 public class Swap {
    public static void main(String[] args){
       int temp;
       int x=2 ,y=3;
       System.out.println("Before Swaping:");
       System.out.println(" x="+x);
       System.out.println(" y="+y);
       temp=x;
       x=y;
       y=temp;
       System.out.println("After swaping:");
       System.out.println(" x="+x);
       System.out.println(" y="+y);
       

    }
}
